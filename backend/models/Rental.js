const mongoose = require('mongoose');

const rentalSchema = new mongoose.Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  vehicleType: {
    type: String,
    required: true,
  },
  pickupDate: {
    type: Date,
    required: true,
  },
  returnDate: {
    type: Date,
    required: true,
  },
});

module.exports = mongoose.model("rental", rentalSchema);





